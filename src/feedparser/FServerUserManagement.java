    private void doNewRequest() {
        String configString = CommonTasks.getBasicConfigString();
        //        System.out.println("\nLocal Output: The config String is: \n" + configString);
        ArrayList<String> lines = this.postUrl(CommonTasks.getUrlNewRequestRegister(), configString, "ELN>");
        DefaultTableModel model = (DefaultTableModel)tblNewRequest.getModel();
        model.setRowCount(0);
        for (String line : lines) {
            line = line.substring(4);
            String[] toke = line.split(",");
            Vector v = new Vector();
            v.add(toke[0]);
            v.add(toke[1]);
            v.add(toke[2]);
            v.add(toke[3]);
            v.add(toke[4]);
            v.add(toke[5]);
            
            model.addRow(v);
        }

    }

