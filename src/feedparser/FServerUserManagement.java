    private ArrayList<String> postUrl(String baseUrl, String pvalue, String lineIdentifier) {

        String url = baseUrl;
        url = url + pvalue;
        ArrayList lines = new ArrayList<String>();

        try {
            Document doc = Jsoup.connect(url).get();
            Element body = doc.body();
            HtmlToPlainText h = new HtmlToPlainText();
            String plain = h.getPlainText(body);
            String[] toke = plain.split("\n");
            System.out.println("Total " + toke.length + " lines returned");
            for (int i = 0; i <= toke.length - 1; i++) {
                String line = toke[i];
                if (line.startsWith(lineIdentifier)) {
                    lines.add(line);
                    System.out.println(line);
                }
            }
        } catch (IOException ioe) {

        }

        return lines;
    }

